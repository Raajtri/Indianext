'use client'

import { useState } from 'react'
import { AlertTriangle, Info, Shield, ThumbsUp, ThumbsDown, ChevronDown, ChevronUp, FileText, Link as LinkIcon, Video } from 'lucide-react'

interface ExplanationPanelProps {
  explanation: string[]
  threatType?: string
  confidence?: number
}

export function ExplanationPanel({ explanation, threatType, confidence }: ExplanationPanelProps) {
  const [isExpanded, setIsExpanded] = useState(false)

  const getThreatIcon = () => {
    switch(threatType) {
      case 'phishing':
        return <FileText className="h-5 w-5 text-orange-500" />
      case 'malicious-url':
        return <LinkIcon className="h-5 w-5 text-red-500" />
      case 'deepfake':
        return <Video className="h-5 w-5 text-purple-500" />
      default:
        return <Shield className="h-5 w-5 text-green-500" />
    }
  }

  const getThreatColor = () => {
    if (!threatType || threatType === 'safe') return 'green'
    return threatType === 'phishing' ? 'orange' : threatType === 'malicious-url' ? 'red' : 'purple'
  }

  const color = getThreatColor()

  return (
    <div className="bg-white dark:bg-slate-800 rounded-xl shadow-lg border border-slate-200 dark:border-slate-700 overflow-hidden">
      {/* Header */}
      <div className={`p-4 border-b border-slate-200 dark:border-slate-700 bg-${color}-50 dark:bg-${color}-900/20`}>
        <div className="flex items-center justify-between">
          <div className="flex items-center gap-3">
            {getThreatIcon()}
            <div>
              <h3 className="font-semibold text-slate-900 dark:text-white">
                AI Analysis Explanation
              </h3>
              <p className="text-sm text-slate-600 dark:text-slate-400">
                Why this content was flagged
              </p>
            </div>
          </div>
          <button
            onClick={() => setIsExpanded(!isExpanded)}
            className="p-2 hover:bg-slate-200 dark:hover:bg-slate-700 rounded-lg transition-colors"
          >
            {isExpanded ? (
              <ChevronUp className="h-5 w-5 text-slate-500" />
            ) : (
              <ChevronDown className="h-5 w-5 text-slate-500" />
            )}
          </button>
        </div>
      </div>

      {/* Explanation Content */}
      <div className={`p-4 transition-all ${isExpanded ? 'block' : 'line-clamp-2'}`}>
        <div className="space-y-3">
          {explanation.map((reason, index) => (
            <div key={index} className="flex items-start gap-3">
              {threatType && threatType !== 'safe' ? (
                <ThumbsDown className="h-4 w-4 text-red-500 flex-shrink-0 mt-0.5" />
              ) : (
                <ThumbsUp className="h-4 w-4 text-green-500 flex-shrink-0 mt-0.5" />
              )}
              <p className="text-sm text-slate-700 dark:text-slate-300">{reason}</p>
            </div>
          ))}
        </div>
      </div>

      {/* Confidence Score (if provided) */}
      {confidence !== undefined && (
        <div className="px-4 py-3 bg-slate-50 dark:bg-slate-700/50 border-t border-slate-200 dark:border-slate-700">
          <div className="flex items-center justify-between">
            <span className="text-sm text-slate-600 dark:text-slate-400">Model Confidence</span>
            <div className="flex items-center gap-2">
              <div className="w-24 h-2 bg-slate-200 dark:bg-slate-600 rounded-full overflow-hidden">
                <div 
                  className={`h-full rounded-full bg-${color}-500`}
                  style={{ width: `${confidence}%` }}
                />
              </div>
              <span className={`text-sm font-medium text-${color}-600 dark:text-${color}-400`}>
                {confidence}%
              </span>
            </div>
          </div>
        </div>
      )}

      {/* AI Disclaimer */}
      <div className="px-4 py-3 bg-blue-50 dark:bg-blue-900/20 border-t border-slate-200 dark:border-slate-700">
        <div className="flex items-start gap-2">
          <Info className="h-4 w-4 text-blue-600 dark:text-blue-400 flex-shrink-0 mt-0.5" />
          <p className="text-xs text-blue-700 dark:text-blue-300">
            This analysis is generated by AI and should be used as a guideline. 
            Always exercise caution and verify through official channels when in doubt.
          </p>
        </div>
      </div>
    </div>
  )
}